            <h1 class="center">História</h1>
            <h3 class="center">Lê aqui um resumo da história de Haikyuu.</h3>
                        
            <p> O protagonista chama-se Hinata. Ele, depois de ver um jogo da escola Karasuno na televisão, onde o "Pequeno Gigante" jogava, apaixonou-se por esse desporto e decidiu então que seria ele o novo "Pequeno Gigante". Esse passou a ser o seu sonho.</p>

            <p> Ainda no ensino fundamental ele tentou entrar em um clube de voleibol, mas, na sua escola, não haviam integrantes, o que o impossibilitava de realizar tal feito. Ele passou então a treinar sozinho e com a ajuda de amigos dos clubes de futebol e basquete ele entrou em uma competição onde teve de enfrentar Kegeyama Tobio.</p>

            <p> Kageyama Tobio é um distribuidor com um talento nato incrível, rabugento, insensível e sem nenhuma noção do que é trabalho de equipa, ele é conhecido como "Rei do Campo". Hinata acredita que é porque ele é muito bom, mas no decorrer da história descobrimos mais um pouco sobre esse nome. Num jogo nem um pouco justo, pois os amigos de Hinata não são nem um pouco talentosos no voleibol, mesmo assim Kageyama reconhece o poder de saltos de Hinata, pois ele é de baixa estatura, e para poder jogar voleibol ele tem de saltar muito.</p>

            <p> Depois da partida Hinata e Kageyama se reencontram, e Hinata diz que quer revanche, declarando guerra ao distribuidor Kageyama.</p>
                        
            <p> Bem, o que acontece a seguir? Hinata entra no Colegio Karasuno, a mesma de ídolo, e pretende entrar no clube de voleibol, o que ele não esperava é que a partir dai fosse obrigado a jogar lado a lado com seu maior arqui-inimigo, Kageyama.</p>
